import 'abstract_mc.dart';
import '../content_builder/wa_document_content_builder.dart';

class WaSendDocument extends AbstractMc {
  @override
  String action() {
    return 'send-whatsapp';
  }

  @override
  List requiredKey() {
    return ['from', 'to', 'content'];
  }

  WaSendDocument from(String id, [String type = 'bot_username']) {
    params['from'] = {
      'type': type,
      'id': id,
    };
    return this;
  }

  WaSendDocument to(String id, [String type = 'phone_num']) {
    params['to'] = {
      'type': type,
      'id': id,
    };
    return this;
  }

  WaSendDocument content(WaDocumentContentBuilder contentBuilder) {
    params['content'] = contentBuilder.requestData;
    return this;
  }
}
